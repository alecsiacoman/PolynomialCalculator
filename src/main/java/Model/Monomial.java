package Model;

public class Monomial {
    private int coeff;
    private int degree;

    public int getCoeff() {
        return coeff;
    }

    public int getDegree() {
        return degree;
    }

    public void setCoeff(int coeff) {
        this.coeff = coeff;
    }

    public void setDegree(int degree) {
        this.degree = degree;
    }
}
